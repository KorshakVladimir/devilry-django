function ajaxGrid()
{
    function renderScaledPoints(val) {
        if (val > 12) {
            return '<span style="color:green;">' + val + '</span>';
        } else if (val == 11) {
            return '<span style="color:red;">' + val + '</span>';
        }
        return val;
    }


    Ext.define('AssignmentGroups',{
        extend: 'Ext.data.Model',
        fields: [
            // set up the fields mapping into the xml doc
            // The first needs mapping, the others are very basic
            'status',
            'scaled_points',
            'candidates__student__username',
        ]
    });

    // create the Data Store
    var store = Ext.create('Ext.data.Store', {
        model: 'AssignmentGroups',
        proxy: {
            // load using HTTP
            type: 'ajax',
            url: 'assignmentgroups-qry',
            // the return will be XML, so lets set up a reader
            reader: {
                type: 'json',
                //record: 'Item',
                //totalProperty  : 'total'
            }
        }
    });

    // create the grid
    var grid = Ext.create('Ext.grid.Panel', {
        store: store,
        columns: [
            {text: "Status", width: 120, dataIndex: 'status', sortable: true},
            {text: "Scaled points", flex: 1, dataIndex: 'scaled_points', sortable: true, renderer: renderScaledPoints},
            {text: "Username", width: 100, dataIndex: 'candidates__student__username', sortable: true}
        ],
        viewConfig: {
            forceFit: true
        },
        height:210,
        split: true,
        region: 'north'
    });
        
    // define a template to use for the detail view
    var bookTplMarkup = [
        'Scaled points: {scaled_points}<br/>',
        'Status: {status}<br/>',
        'Username: {candidates__student__username}<br/>'
    ];
    var bookTpl = Ext.create('Ext.Template', bookTplMarkup);

    Ext.create('Ext.Panel', {
        renderTo: 'binding-example',
        frame: true,
        title: 'Book List',
        width: 540,
        height: 400,
        layout: 'border',
        items: [
            grid, {
                id: 'detailPanel',
                region: 'center',
                bodyPadding: 7,
                bodyStyle: "background: #ffffff;",
                html: 'Please select a book to see additional details.'
        }]
    });
    
    // update panel body on selection change
    grid.getSelectionModel().on('selectionchange', function(sm, selectedRecord) {
        if (selectedRecord.length) {
            var detailPanel = Ext.getCmp('detailPanel');
            bookTpl.overwrite(detailPanel.body, selectedRecord[0].data);
        }
    });

    store.load();

}






